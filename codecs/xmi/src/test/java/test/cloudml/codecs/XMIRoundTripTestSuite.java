import java.io.File;
import java.io.FilenameFilter;
import org.cloudml.codecs.XmiCodec;

/**
 * Unit test for rountrip testing of CloudML models.
 */
public class XMIRoundTripTestSuite extends RoundTripTestSuite {


    public XMIRoundTripTestSuite() {
        super();
        setCodec(new XmiCodec());
        FilenameFilter xmiFilter = new FilenameFilter() {

            public boolean accept(File dir, String name) {
                return name.endsWith(".xmi");
            }
        };
        setFilter(xmiFilter);
        init();
    }

  
}
