package org.cloudml.ui.shell.terminal;

/**
 * Colours available on ANSI console
 */
public enum Color {
    BLACK("\u001B[30m"),
    RED("\u001B[31m"),
    GREEN("\u001B[32m"),
    YELLOW("\u001B[33m"),
    BLUE("\u001B[34m"),
    PURPLE("\u001B[35m"),
    CYAN("\u001B[36m"),
    WHITE("\u001B[37m");
    
    
    private final String flag;

    private Color(String flag) {
        this.flag = flag;
    }

    public String paint(String message) {
        return flag + message + "\u001B[0m";
    }
    
}
